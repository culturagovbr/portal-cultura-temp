                        <p>Dessa forma, o incentivo cumpre interesses públicos ao promover maior democratização do acesso aos produtos culturais criados pelos projetos, tendo em vista a efetivação de direitos culturais, e a contribuição para a evolução da economia criativa, fundamental para o desenvolvimento do País.</p>
