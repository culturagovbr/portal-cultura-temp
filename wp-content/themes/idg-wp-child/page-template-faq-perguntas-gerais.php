                        <p>Pelo mecanismo de incentivo fiscal da Lei Federal de Incentivo à Cultura, o Governo Federal abre mão de receber parcela do imposto de renda devido de pessoas físicas ou empresas tributadas com base no lucro real para que apoiem financeiramente projetos culturais aprovados pela Secretaria Especial da Cultura do Ministério da Cidadania. A pessoa física ou jurídica que apoia financeiramente o projeto tem até 100% do valor desembolsado deduzido do imposto devido (artigo 18 da Lei 8.313/1991), dentro dos percentuais permitidos pela legislação tributária.</p>

