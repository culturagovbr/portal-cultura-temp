    <div class="container image-left align-center">
      <div class="col-text">
        <h3>Como patrocinar?</h3>

        <p>Qualquer pessoa física que tenha imposto de renda a declarar ou empresa tributada pelo lucro real pode financiar projetos culturais utilizando o mecanismo da <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei de Incentivo à Cultura</a>, sendo que as pessoas físicas podem direcionar 6% do imposto devido e as jurídicas, 4%.</p>

        <p>Há duas formas de financiar um projeto: por meio de doação ou de patrocínio. A doação é um repasse sem retorno de imagem para o incentivador. É um apoio que resulta apenas da decisão de aplicar parcela do imposto de renda devido em um projeto cultural para o qual a pessoa ou empresa queira contribuir. O patrocínio é um repasse com retorno de imagem. Além de viabilizar a realização de um projeto, o patrocinador se beneficia de estratégias de comunicação, assinando o patrocínio com sua marca e inserindo sua imagem associada ao projeto selecionado, conforme Artigo 23, da <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei 8.313/91</a>. A doação ou patrocínio não pode ser feita a projeto de pessoa ou instituição vinculada ao apoiador, de acordo com o Artigo 27 da <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei 8.313/91</a>.</p>

        <p>Ao escolher o projeto que será apoiado, o apoiador precisa estar atento às faixas de renúncia. Os projetos podem ser enquadrados no Artigo 18 da Lei 8.313/91, que permite restituição de 100% do valor financiado dentro do limite de 6% ou 4% do imposto devido, ou podem estar enquadrados no Artigo 26 da Lei e, neste caso, o percentual de restituição vai variar conforme a natureza do apoio (doação ou patrocínio) e o tipo de pessoa (física ou jurídica).</p>
      </div>
    </div>
  </section>


  <section class="image-and-text bg-grey-2" id="inscricao">
    <div class="container image-left align-center">
      <div class="col-text">
        <h3>Faixas de Renúncia</h3>

        <p>Os projetos enquadrados no Artigo 18 da Lei 8.313/91 permitem abatimento de 100% do valor efetivamente despendido pelo patrocinador ou apoiador. São eles:</p>

        <ul>
          <li>Artes cênicas;</li>
          <li>Livros de valor artístico, literário ou humanístico;</li>
          <li>Música erudita ou instrumental;</li>
          <li>Exposições de artes visuais;</li>
          <li>Doações de acervos para bibliotecas públicas, museus, arquivos públicos e cinematecas, bem como treinamento de pessoal e aquisição de equipamentos para a manutenção desses acervos;</li>
          <li>Produção de obras cinematográficas e videofonográficas de curta e média metragem e preservação e difusão do acervo audiovisual;</li>
          <li>Preservação do patrimônio cultural material e imaterial;</li>
          <li>Construção e manutenção de salas de cinema e teatro, que poderão funcionar também como centros culturais comunitários, em municípios com menos de cem mil habitantes.</li>
        </ul>

        <p>Todos os demais projetos que não estão previstos no Artigo 18 se enquadram no Artigo 26, que prevê renúncia fiscal parcial, conforme o tipo de pessoa (física ou jurídica) e a natureza do investimento (doação ou patrocínio):</p>

        <ul>
          <li>Se uma pessoa física quiser financiar um projeto da <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei de Incentivo à Cultura</a> como doação, poderá abater até 80% do valor doado, dentro do limite de 4% já estipulado pela legislação tributária. Já no apoio como patrocínio, o percentual de renúncia é de 60%.</li>
          <li>No caso de uma empresa tributada em lucro real, a renúncia para doação (sem exploração de imagem) a um projeto da <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei de Incentivo à Cultura</a> é de 40%. Se a empresa quiser ter sua imagem associada ao projeto cultural, o apoio se dará via patrocínio e o percentual de renúncia é de 30%.</li>
        </ul>

        <p>Ao apoiar projetos enquadrados no Artigo 26, a pessoa jurídica tributada com base no lucro real ainda poderá abater as doações e patrocínios como despesa operacional. Nesse caso, haverá diminuição do lucro resultante e, consequentemente, do imposto de renda a pagar. Segundo a Receita Federal, são “operacionais as despesas não computadas nos custos, necessárias à atividade da empresa e à manutenção da respectiva fonte produtora, entendendo-se como necessárias as pagas ou incorridas para a realização das transações ou operações exigidas pela atividade da empresa.” Assim, o percentual de dedução do imposto de renda para apoiar projetos do Artigo 26 da Lei Federal de Incentivo à Cultura atinge, na prática, os seguintes percentuais para a pessoa jurídica:</p>

        <ul>
          <li>Para doação, os percentuais a serem deduzidos variam entre 65% e 70%.</li>
          <li>Para patrocínio, entre 55% e 60%.</li>
        </ul>
      </div>
    </div>
