        <h3>Como inscrever seu projeto?</h3>

        <p>A inscrição de um projeto na <a href="http://www.planalto.gov.br/ccivil_03/leis/L8313cons.htm" target="_blank">Lei de Incentivo à Cultura</a> é feita pelo <a href="http://salic.cultura.gov.br/autenticacao/index/index" target="_blank">Sistema de Apoio às Leis de Incentivo à Cultura (Salic)</a>. No sistema, é preciso cadastrar a proposta e descrevê-la detalhadamente, relatar suas razões e objetivos, especificar como e em quanto tempo será executada, como será divulgada, quais profissionais e empresas estarão envolvidos nesse processo e quanto custará sua execução, detalhando item a item do orçamento. Também é preciso acrescentar as ações de contrapartida social que serão oferecidas.</p>

        <p>São aceitos projetos de incentivo à formação artística e cultural; fomento à criação artística; preservação e difusão do patrimônio artístico, cultural e histórico; estímulo ao conhecimento de bens e valores culturais e de apoio a outras atividades culturais e artísticas. Ou seja, é possível propor a restauração ou construção de um centro cultural, planos anuais de manutenção de espaços culturais, orquestras, museus, companhias de teatro e dança, construção de bibliotecas e de projetos literários itinerantes, produção de shows, feiras, espetáculos, livros, festivais, música, etc. </p>

        <p>Qualquer pessoa pode ser um proponente, que é o termo que designa a pessoa responsável por apresentar, realizar e responder pelo projeto cultural. Pode ser uma pessoa física com atuação na área cultural ou pessoa jurídica de direito público ou privado, com ou sem fins lucrativos, como uma empresa, produtora, instituição ou fundação, cujo ato constitutivo ou instrumento congênere disponha sobre sua finalidade cultural.</p>
