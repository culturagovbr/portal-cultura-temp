                  <div id="faq-21" class="card-header">
                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-21" aria-expanded="false" aria-controls="collapse-faq-21"><span>Como funciona a conta vinculada e a movimentação dos recursos?</span></a></h3>
                  </div>
                  <div id="collapse-faq-21" class="collapse" aria-labelledby="faq-21" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>Após a publicação da Portaria que autoriza a captação de recursos incentivados no Diário Oficial da União (DOU), é aberta uma conta no Banco do Brasil vinculada ao CPF ou ao CNPJ do proponente para o qual o projeto tenha sido aprovado.</p>
                        <p>Os depósitos de doação e/ou patrocínio serão realizados nesta conta vinculada ao projeto com a devida identificação. Os recursos podem ser movimentados por intermédio de cartão magnético ou gerenciador financeiro.</p>
                        <p>A primeira movimentação dos recursos para o cartão da conta vinculada será efetuada pelo MinC após consulta da regularidade dos proponentes. Para isso, serão utilizadas trilhas de controle para o cruzamento de dados de pessoas físicas ou jurídicas e seus dirigentes junto ao Sistema de Apoio às Leis de Incentivo à Cultura (Salic). Neste momento, também devem ser comprovadas a Certidão Negativa de Débitos Relativos aos Tributos Federais (CQTF) e o Cadastro Informativo de Créditos Não Quitados do Setor Público Federal (CADIN).</p>
                        <p>Ainda é necessário que o projeto tenha sido homologado e tenha atingido o percentual mínimo de 20% de captação do valor total do custo para que os recursos sejam movimentados da conta vinculada. No caso de projetos classificados como plano anual ou plurianual de atividades, desde que homologados, os recursos captados poderão ser movimentados quando atingido 1/12 do orçamento global para projetos de plano anual e 1/24 do orçamento global para projetos de plano plurianual.</p>
                     </div>
