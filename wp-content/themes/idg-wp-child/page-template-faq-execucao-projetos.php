                  <div id="collapse-faq-18" class="collapse" aria-labelledby="faq-18" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>O proponente poderá ser remunerado com recursos decorrentes de renúncia fiscal, desde que preste serviço ao projeto, conforme previsto no orçamento apresentado.</p>
