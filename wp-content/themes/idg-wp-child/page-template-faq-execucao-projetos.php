                     <h3><a class="collapsed" href="#this" data-toggle="collapse" data-target="#collapse-faq-22" aria-expanded="false" aria-controls="collapse-faq-22"><span>É possível a transferência de recursos de um projeto para outro?</span></a></h3>
                  </div>
                  <div id="collapse-faq-22" class="collapse" aria-labelledby="faq-22" data-parent="#accordionExample">
                     <div class="card-body">
                        <p>Em alguns casos, os recursos captados por um projeto podem ser transferidos para outro:</p>
                        <p>Planos anuais e plurianuais de atividades ou projeto de ação continuada do mesmo proponente. Nesse caso, o saldo transferido deverá somar-se aos recursos já captados até atingir os limites de movimentação financeira do projeto vigente.</p>
                        <p>Se um projeto não for homologado para execução pela CNIC, será facultada a transferência dos recursos já captados para um único projeto aprovado do mesmo proponente. Nesse caso, é necessária a apresentação de justificativa do proponente e a anuência do incentivador, caso pessoa jurídica. Se o incentivador for pessoa física, basta que o proponente comunique a transferência dos recursos. Assim, o projeto que tiver seus recursos transferidos para outro é arquivado definitivamente.</p>
                        <p>Caso o projeto que não receber a homologação seja da área de patrimônio cultural, museus e memória, os recursos captados desde a aprovação poderão ser transferidos para outro projeto já aprovado do mesmo proponente ou para outros projetos de proponente diverso. Nesse caso, é necessária a apresentação da anuência formalizada pelo proponente do projeto transferidor e pelo incentivador. A transferência de recursos deve ser analisada e aprovada pelo Instituto do Patrimônio Histórico e Artístico Nacional (Iphan) ou Instituto Brasileiro de Museus (Ibram), instituições vinculadas ao MinC.</p>
